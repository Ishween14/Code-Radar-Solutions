#include <stdio.h> // Include the standard input/output library

int main() {
    float a; // Declare a variable to store a floating-point number

    printf("enter a:"); // Prompt the user to enter a value
    scanf("%f", &a); // Read the floating-point value entered by the user and store it in 'a'
    printf("You entered:%f\n", a); // Print the entered value

    return 0; // Indicate that the program has executed successfully
}
